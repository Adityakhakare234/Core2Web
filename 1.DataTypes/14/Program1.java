class Demo1 {
	
	public static void main(String[] args) {
		
		char ch = 'A'; // Stores the A as a single char
		int num1 = ch; // Stores the ASCII Value of 'A' = 65 
		float num2 = num1; // Stores 65 as 65.0
		double num3 = num2; // Stores 65.0 to 65.0

		System.out.println(ch);
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
	}
}
