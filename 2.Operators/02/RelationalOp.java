//Relational Operators are :- <,>,==,<=,>=,!=

class Relational {
	
	public static void main(String[] args) {
		
		int num1 = 7;
		int num2 = 5;

		System.out.println(num1 > num2);   // True
		System.out.println(num1 < num2);   // False
		System.out.println(num1 == num2); // False
		System.out.println(num1 != num2); // True
		System.out.println(num1 >= num2); // True
		System.out.println(num1 <= num2); // False
	}
}
